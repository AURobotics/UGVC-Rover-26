void nrf_task(void* argument) {
  uint8_t i2c_rx_buf[3];
  int8_t left_wheel_vel;
  int8_t right_wheel_vel;
  uint8_t estop_pressed;
  char cdc_msg_buf[100];

  while (1) {
    HAL_StatusTypeDef i2c_status = HAL_I2C_Master_Receive(&hi2c1, ARDUINO_I2C_ADDR, i2c_rx_buf, 3, 50);

    if (i2c_status == HAL_OK) {
      estop_pressed   = i2c_rx_buf[2];

      int msg_len = sprintf(cdc_msg_buf, " E-Stop: %d\r\n", estop_pressed);
      CDC_Transmit_FS((uint8_t*)cdc_msg_buf, msg_len);

      if (estop_pressed) {
        // E-stop takes priority: zero the command instead of forwarding motion
        pwm_m.disable_output();
        pwm_s.disable_output();
      }
	} else {
      uint32_t error_code = HAL_I2C_GetError(&hi2c1);
      uint32_t state_code = HAL_I2C_GetState(&hi2c1);
      int len = sprintf(cdc_msg_buf, "I2C Error! Status: %d | HAL Error Code: 0x%02lX | State: 0x%02lX\r\n",
                         i2c_status, error_code, state_code);
      CDC_Transmit_FS((uint8_t*)cdc_msg_buf, len);
    }

    vTaskDelay(pdMS_TO_TICKS(30));
  }
}
