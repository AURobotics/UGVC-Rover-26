void cdc_read_task(void* argument) {
  while (1) {
    if (cdc.available()) {
      cdc.read_msg(rx_msg);

      switch(rx_msg.type) {
        case Message_Type::MSG_CMD_VEL:
          cmd_ctrl = 1;
	      xQueueOverwrite(cmdVelQueue, &rx_msg.data.cmd_vel_msg);
          break;
        case Message_Type::MSG_SERVO:
          xQueueOverwrite(servoQueue, &rx_msg.data.servo_msg);
          break;
        case Message_Type::MSG_LASER:
          xQueueOverwrite(laserQueue, &rx_msg.data.laser_msg);
          break;
        default:
          break;
      }
    }
    vTaskDelay(pdMS_TO_TICKS(10));
  }
}

void motor_task(void* argument) {
  uint32_t last_cmd_ctrl = 0, now;
  CmdVelPayload cmd_msg;

  while (1) {
    now = HAL_GetTick();
    if (xQueuePeek(cmdVelQueue, &cmd_msg, 0) && (cmd_ctrl == 1)) {
      M_fl.update_control(cmd_msg.left_wheel);
      M_bl.update_control(cmd_msg.left_wheel);
      M_fr.update_control(cmd_msg.right_wheel);
      M_br.update_control(cmd_msg.right_wheel);
	  cmd_ctrl = 0;
      last_cmd_ctrl = now;
    }
    if (now - last_cmd_ctrl > 500) {
      M_fl.move(0);
      M_bl.move(0);
      M_fr.move(0);
      M_br.move(0);
    }

    HAL_StatusTypeDef status = pwm_m.write();

    vTaskDelay(pdMS_TO_TICKS(5));
  }
}

